public class Land {
    private double length;
    private double width;

    public Land() {
        this.length = 0;
        this.width = 0;
    }

    public Land(double length, double width) {
        this.length = length;
        this.width = width;
    }

    public double getLength() {
        return length;
    }

    public double getWidth() {
        return width;
    }
}